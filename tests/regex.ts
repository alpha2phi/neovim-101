/**
 * `@hello`
 * One of `a-z`
 */
/@hello[a-z]/;

/**
 * One of **WB**, **WORD**, **0-9**, **WS**, **TAB**, **LF**, or **CR**
 */
/[\b\w\d\s\t\n\r]/;

/**
 * **START**
 * One of `a-z`, `A-Z`, or `0-9` (_6-12x_)
 * **END**
 */
/^[a-zA-Z0-9]{6,12}$/;

/**
 * One of `-`, **WORD**, or `.`
 */
/[-\w.]/;

/**
 * One of **WORD**, `,`, or `.`
 */
/[\w,.]/;

/**
 * One of **WORD**, `-`, or `.`
 */
/[\w\-.]/;

/**
 * One of `.`, `-`, or **WORD**
 */
/[.\-\w]/;

/**
 * **0-9** (_>= 0x_)
 * ` `
 */
/\d*\ /;

/**
 * `a` (_1x_)
 * `b` (_>= 2x_)
 * `c` (_3-5x_)
 * `d` (_>= 0x_)
 * `e` (_>= 1x_)
 */
/a{1}b{2,}c{3,5}d*e+/g;

/**
 * **WB**
 * One of `a-z`, `0-9`, `.`, `\_`, `%`, `+`, or `-` (_>= 1x_)
 * `@hello`
 * One of `a-z`, `0-9`, `.`, or `-` (_>= 1x_)
 * `.`
 * One of `a-z` (_>= 2x_)
 * **WB**
 */
/\b[a-z0-9._%+-]+@hello[a-z0-9.-]+\.[a-z]{2,}\b/;
